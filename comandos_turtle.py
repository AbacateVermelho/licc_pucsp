import turtle               # chamar o turtle
tartaruga = turtle.Turtle() # definir o turtle
tartaruga.shape('turtle')   # definir o formato do turtle
tartaruga.color('purple')   # definir a cor do turtle
tartaruga.forward()         # movimentar para a frente
tartaruga.backward()        # movimentar para tras
tartaruga.left()            # movimentar para a esquerda
tartaruga.right()           # movimentar para a direita
tartaruga.speed()           # velocidade
tartaruga.up()              # 'levanta' a caneta
tartaruga.down()            # 'abaixa' a  caneta
